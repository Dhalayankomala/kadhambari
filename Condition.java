import java.util.Scanner;
class Condition
{
public static void main(String args[])
{
Scanner s=new Scanner(System.in);
int a=s.nextInt();
if(a==1)
{
System.out.println("monday");
}
elseif(a==2);
{
System.out.println("Tuesday");
}
elseif(a==3);
{
System.out.println("wednesday");
}
elseif(a==4);
{
System.out.println("Thursday");
}

}
}